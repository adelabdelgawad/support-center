/**
 * API route for toggling Organizational Unit enabled status
 * POST /api/organizational-units/[id]/toggle - Toggle OU enabled status
 */

import { NextRequest, NextResponse } from 'next/server';
import { makeAuthenticatedRequest, getServerErrorMessage } from '@/lib/api/server-fetch';

interface EnhancedError extends Error {
  status?: number;
  detail?: string;
}

/**
 * POST /api/organizational-units/[id]/toggle
 * Toggles organizational unit enabled status
 */
export async function POST(
  _request: NextRequest,
  { params }: { params: Promise<{ id: string }> }
) {
  const { id } = await params;
  try {
    const body = await _request.json();
    const response = await makeAuthenticatedRequest<unknown>('POST', `/organizational-units/${id}/toggle`, body);
    return NextResponse.json(response);
  } catch (error: unknown) {
    const message = getServerErrorMessage(error);
    const status = (error as EnhancedError).status || 500;
    return NextResponse.json({ detail: message }, { status });
  }
}
